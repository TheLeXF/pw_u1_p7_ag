function cambiarColorTexto(idelemento,color){
    document.getElementById(idelemento).style.color=color
}

function cambiarTitulo(idelemento){
    document.getElementById(idelemento).innerText='JS Externo';
}

function alinearTexto(idelemento, alineacion){
    document.getElementById(idelemento).style.textAlign =alineacion;
}

function tamañoFuente(idelemento, tamaño){
    document.getElementById(idelemento).style.fontSize=tamaño;
}

function colorBackground(idelemento,color){
    document.getElementById(idelemento).style.backgroundColor=color;
}

